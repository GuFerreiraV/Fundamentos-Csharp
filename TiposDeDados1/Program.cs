// Tipos de valores númericos inteiros em C#

sbyte sByteValue = -21; // -128 to 127
byte byteValue = 21; // 0 to 255
int intValue = 21; // -2,147,483,648 to 2,147,483,647
uint uintValue = 2123123121; // 0 to 4,294,967,295
long longValue = -1000000000; // -9,223,372,036,854,775,808 to 9,223,372,036,854,775,807

Console.WriteLine("sbyte: " + sByteValue);
Console.WriteLine("byte: " + byteValue);
Console.WriteLine("int: " + intValue);
Console.WriteLine("uint: " + uintValue);
Console.WriteLine("byte: " + longValue);

Console.ReadLine();